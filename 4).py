a = int(input("введите коэффицент а: "))
b = int(input("введиет коэффицент b: "))
c = int(input("введиет коэффицент c: "))
D = b**2 - 4 * a * c
if D > 0: 
    x1 = (-b + D**(1/2)) / (2 * a)
    x2 = (-b - D**(1/2)) / (2 * a)
    print("x1 =",x1,";","x2 =",x2)
if D == 0:
    x1 = (-b)/(2 * a)
    print("корень x1 =", x1)
if D < 0: 
    print("корней нет :( ")

